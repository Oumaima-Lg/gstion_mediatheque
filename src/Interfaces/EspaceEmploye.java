    private void liste_doc_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_liste_doc_btnActionPerformed
        // TODO add your handling code here
        subPanel_modifier_doc.setVisible(false);
        subPanel_ajouter_doc.setVisible(false);
        subPanel_ajouter_auteur.setVisible(false);

        liste_doc_btn.setVisible(false);
        modifier_doc_btn.setVisible(false);
        ajouter_doc_btn.setVisible(false);

        subPanel_liste_doc.setVisible(true);

        try {
            DefaultTableModel tableModel = (DefaultTableModel) jTable4.getModel();
            tableModel.setRowCount(0);

            ResultSet r = Operateur.displayDocuments();
            Object[] rowData = null;
            while (r.next()) {
                if( r.getString(3).equals("Journal") || r.getString(3).equals("Microfilm") ){
                    rowData = new Object[] {r.getLong(1), r.getString(2), r.getString(3), r.getString(5), r.getDate(4), 
                    r.getString(6), r.getString(7), r.getDouble(8), r.getInt(9)};
                    tableModel.addRow(rowData);
                }
                else {
                    rowData = new Object[] {r.getLong(1), r.getString(2), r.getString(3), r.getString(5),
                    "---", r.getString(6), r.getString(7), r.getDouble(8), r.getInt(9)};
                    tableModel.addRow(rowData);
                }     
            }
            jTable4.setModel(tableModel);
            r.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Une erreur de connexion à la base de données est survenue. Veuillez réessayer plus tard.",
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Une erreur s'est produite. Veuillez réessayer plus tard.",
                    "Erreur", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_liste_doc_btnActionPerformed

