    private void ajouterDocumentBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ajouterDocumentBtnActionPerformed
        // TODO add your handling code here:
        String typeDocument = typeDoc.getSelectedItem().toString();

        String titreDocument = titreDoc.getText();

        int quantiteDocument = Integer.parseInt(quantiteDoc.getText());

        String empruntableSelection = "";
        double cautionDocument = 0;
        java.sql.Date dateDocument = null;

        if ((typeDocument.equals("Journal")) || (typeDocument.equals("Microfilm"))) {
            java.util.Date date = doc_date.getDate();
            dateDocument = new java.sql.Date(date.getTime());
            empruntableSelection = "Non";
            cautionDocument = 0;

            subPanel_ajouter_auteur.setVisible(false);
        } else if ((typeDocument.equals("Livre")) || (typeDocument.equals("CD-ROM"))) {
            java.time.LocalDate currentDate = java.time.LocalDate.now();
            dateDocument = java.sql.Date.valueOf(currentDate);
            empruntableSelection = "Oui";
            cautionDocument = Double.parseDouble(cautionDoc.getText());

            subPanel_ajouter_auteur.setVisible(true);
            subPanel_ajouter_doc.setVisible(false);
        }

        Operateur.addDocument(typeDocument, empruntableSelection, titreDocument, dateDocument, quantiteDocument, cautionDocument, acheter_selectDocument, emprunter_selectDocument, idDoc);
    }//GEN-LAST:event_ajouterDocumentBtnActionPerformed

