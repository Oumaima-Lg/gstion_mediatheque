    private void Utiliser_ecran_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Utiliser_ecran_btnActionPerformed
        // TODO add your handling code here:
        String materiel = (String) idEcran.getSelectedItem();
        String titreMicroFilm = (String) titreMICROFILM.getSelectedItem();
        String heures = Heures.getText();
        String minutes = Minutes.getText();

        if ((materiel != null) && (titreMicroFilm != null) && (heures != null) && (minutes != null)) {
            String[] ecran = materiel.split(" - ");
            String[] parts = titreMicroFilm.split(" - ");
            if (ecran.length == 2 && parts.length == 2) {
                int h = Integer.parseInt(heures);
                int m = Integer.parseInt(minutes);
                int durée = h * 60 + m;
                long cote = Long.parseLong(parts[0]);
                int idE = Integer.parseInt(ecran[0]);
                Operateur.utiliserMateriel(idE, cote, idEcran, Materiel, durée);
            }
        }
    }//GEN-LAST:event_Utiliser_ecran_btnActionPerformed

